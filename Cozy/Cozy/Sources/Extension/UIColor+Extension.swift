import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var coolGreen: UIColor {
        return UIColor(red: 46.0 / 255.0, green: 206.0 / 255.0, blue: 106.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var dustyOrange: UIColor {
        return UIColor(red: 252.0 / 255.0, green: 99.0 / 255.0, blue: 56.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGrey: UIColor {
      return UIColor(white: 112.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey: UIColor {
      return UIColor(white: 163.0 / 255.0, alpha: 1.0)
    }


}
